# creat a list using [] seprated by commas
a = [1, 2, 3, 4, 5]

# any element can be replaced using a[index]
a[0] = 98

# print the list using print() function by print(a)
print(a)

# print perticular index item using print(a[index])
print(a[0])

# we can also create list of dirffrent data type
c = [12, "SAURABH", False, 9.9]
print(type(c[0]))
print(type(c[1]))
print(type(c[2]))
print(type(c[3]))

# lists can also be sliced just like string
print(c[0:3])